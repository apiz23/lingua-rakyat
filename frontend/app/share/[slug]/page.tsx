import { getShare } from "@/lib/api"
import { ChatMarkdown } from "@/components/chat-panel/chat-markdown"
import { notFound } from "next/navigation"
import Link from "next/link"
import type { Metadata } from "next"

type SharePageProps = { params: Promise<{ slug: string }> }

// Strip markdown syntax so OG descriptions read as plain text.
function plainExcerpt(markdown: string, maxLength: number): string {
  const text = markdown
    .replace(/[#*_`>[\]()]/g, "")
    .replace(/\s+/g, " ")
    .trim()
  return text.length > maxLength ? `${text.slice(0, maxLength - 1)}…` : text
}

export async function generateMetadata({
  params,
}: SharePageProps): Promise<Metadata> {
  const { slug } = await params
  const data = await getShare(slug)
  if (!data) return { title: "Shared answer" }

  const title = plainExcerpt(data.question, 70)
  const description = plainExcerpt(data.answer, 160)
  return {
    title,
    description,
    openGraph: {
      title: `${title} – Lingua Rakyat`,
      description,
      type: "article",
      url: `/share/${slug}`,
    },
    twitter: {
      card: "summary_large_image",
      title: `${title} – Lingua Rakyat`,
      description,
    },
  }
}

export default async function SharePage({ params }: SharePageProps) {
  const { slug } = await params
  const data = await getShare(slug)
  if (!data) notFound()

  return (
    <div className="mx-auto max-w-2xl px-4 py-12">
      <div className="mb-2 text-xs font-semibold uppercase tracking-widest text-muted-foreground">
        Lingua Rakyat · Shared answer
      </div>

      <div className="mb-6 rounded-lg border border-border bg-muted/30 px-4 py-3 text-sm text-foreground">
        <span className="font-medium">Q: </span>{data.question}
      </div>

      <div className="prose prose-sm max-w-none dark:prose-invert">
        <ChatMarkdown content={data.answer} />
      </div>

      {data.sources.length > 0 && (
        <div className="mt-6 border-t border-border pt-4">
          <div className="mb-2 text-xs font-semibold text-muted-foreground">Sources</div>
          {data.sources.map((s, i) => (
            <div key={i} className="mb-1 text-xs text-muted-foreground">
              {s.doc_name || s.document_id}{s.page_start ? ` · p.${s.page_start}` : ""}
            </div>
          ))}
        </div>
      )}

      <div className="mt-8 text-center text-xs text-muted-foreground">
        This answer was generated by{" "}
        <Link href="/" className="text-primary underline">Lingua Rakyat</Link>.
        Always verify with official government sources.
      </div>
    </div>
  )
}
